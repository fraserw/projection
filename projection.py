class project():
    def __init__(self, specSlopes, c1_ind=2, c2_ind=4, reference_solar=True):

        """
        Initialize a projection object.

        specSlopes is a two dimensional numpy array. Index 0 corresponds to entries
        of different spectral slope values. Each entry must take the form
            [spec_slope, colour_1, colour_2, colour_3,....]
        and be in sorted order of increasing spectral slope.

        c1_ind and c2_ind are the indexes of the two colours in the specSlopes
        array over which the projection will be made.

        The specSlopes array provided by this module takes the form:
        [spec_slope, u-g, g-r, r-i, r-J, F606w-F814w, F814w-F139m, F139m-F153m]
        and is calculated in synphot with the bk model Solar Spectrum 001.

        reference_solar=True means the projection values are with respect to
        Solar Colours (spec_slope=0). Otherwise the values are with respect to
        the first entry in the specSlopes array.
        """

        self.specSlopes = specSlopes
        self.c1_ind = c1_ind
        self.c2_ind = c2_ind

        self.gmr = specSlopes[:, c1_ind]
        self.rmJ = specSlopes[:, c2_ind]

        self.v1 = np.zeros((len(self.gmr)-1, 2), dtype='float64')
        self.v2 = np.zeros((len(self.gmr)-1, 2), dtype='float64')

        self.v1[:, 0] = self.gmr[1:] - self.gmr[:-1]
        self.v1[:, 1] = self.rmJ[1:] - self.rmJ[:-1]
        self.deltas = np.sum(self.v1**2, axis=1)**0.5
        self.v1[:, 0] /= self.deltas
        self.v1[:, 1] /= self.deltas

        self.v2[:, 0] = -self.v1[:, 1]
        self.v2[:, 1] = self.v1[:, 0]

        self.Deltas = np.cumsum(self.deltas)

        arg = np.argmin(np.abs(specSlopes[:, 0]))

        (self.P_solar, self.C_solar) = self.__getSolarVals(self.specSlopes[:, 0], self.gmr, self.rmJ)
        self.ref_solar = reference_solar

        #self.__interpFunc()
        self.__interpFunc(min_gmr = np.min(self.gmr),
                          max_gmr = np.max(self.gmr),
                          min_rmj = np.min(self.rmJ),
                          max_rmj = np.max(self.rmJ))



    def _getProj(self, GMR, RMJ):

        P = []
        for i in range(len(GMR)):

            dot = self.v1[:, 0]*(GMR[i] - self.specSlopes[1:, self.c1_ind]) + self.v1[:, 1]*(RMJ[i] - self.specSlopes[1:, self.c2_ind])
            arg = np.argmin(np.abs(dot))
            dy = ((GMR[i] - self.specSlopes[arg, self.c1_ind])**2 + (RMJ[i] - self.specSlopes[arg, self.c2_ind])**2)**0.5
            if GMR[i]>self.specSlopes[arg, self.c1_ind]:
                dy*=-1

            P.append([self.Deltas[arg], dy])
        P = np.array(P)

        return P

    def _getColour(self, PC1, PC2):
        k = np.sum(np.less(self.Deltas-self.Deltas[0], PC1))
        ref_c = np.array([self.specSlopes[k-1, self.c1_ind], self.specSlopes[k-1, self.c2_ind]])
        dc = self.v2[k-1, :]*(PC2 - self.P_solar[0][1])

        return ref_c+dc


    def __interpFunc(self, min_gmr = 0.2, max_gmr = 1.4, min_rmj = 0.7, max_rmj = 2.3, step=0.03):
        (self.grid_g, self.grid_j) = np.meshgrid(np.arange(min_gmr, max_gmr+step, step), np.arange(min_rmj, max_rmj+step, step))
        (A, B) = self.grid_g.shape

        pp = self._getProj(self.grid_g.reshape(A*B), self.grid_j.reshape(A*B))
        self.grid_x = pp[:, 0].reshape(A, B)
        self.grid_y = pp[:, 1].reshape(A, B)

        self.f_x = interp.interp2d(self.grid_g[0, :], self.grid_j[:, 0], pp[:, 0].reshape(A,B)-self.P_solar[0, 0])#, kind='linear')
        self.f_y = interp.interp2d(self.grid_g[0, :], self.grid_j[:, 0], pp[:, 1].reshape(A,B)-self.P_solar[0, 1])#, kind='linear')

        return


    def __getSolarVals(self, specSlope, gmr, rmJ):
        f1 = interp.interp1d(specSlope, gmr)
        f2 = interp.interp1d(specSlope, rmJ)

        P_solar = self._getProj(np.array([f1(0.0)]), np.array([f2(0.0)]))
        C_solar = (f1(0.0)-self.gmr[0], f2(0.0)-self.rmJ[0])

        return (P_solar, C_solar)


    def __getProjInterp(self, GMR, RMJ):
        """
        Used only in experimenting.
        """

        return (self.f_x(GMR, RMJ), self.f_y(GMR, RMJ))

    def getProj(self, GMR, RMJ, dGMR, dRMJ):
        """
        Reddening curve project the input colours.

        GMR, and RMJ are the input two colours, and despite their variable names
        correspond to the colours selected at init.

        dGMR and dRMJ are the uncertainties on GMR and RMJ.

        All inputs are numpy arrays of shape (n,)
        """


        P, dP = [], []
        for i in range(len(GMR)):
            ang = np.linspace(0, 2*np.pi, 25)
            X = np.cos(ang)*dGMR[i] + GMR[i]
            Y = np.sin(ang)*dRMJ[i] + RMJ[i]
            x = np.concatenate([np.array([GMR[i]]), X])
            y = np.concatenate([np.array([RMJ[i]]), Y])

            x = np.array([GMR[i], GMR[i]+dGMR[i], GMR[i]-dGMR[i], GMR[i], GMR[i]])
            y = np.array([RMJ[i], RMJ[i], RMJ[i], RMJ[i]+dRMJ[i], RMJ[i]-dRMJ[i]])
            o = self._getProj(x, y)
            (g, j) = o[:,0], o[:,1]
            P.append([g[0], j[0]])
            dP.append([(np.max(g)-np.min(g))/2.0, (np.max(j)-np.min(j))/2.0])
            #dP.append([(np.max(g)-np.min(g))/2.0/1.414, (np.max(j)-np.min(j))/2.0/1.414])

        P = np.array(P)
        dP = np.array(dP)
        if self.ref_solar:
            P -= self.P_solar

        return (P, dP)

    def getColour(self, PC1, PC2, dPC1, dPC2):
        """
        Return a colour and uncertainty given the provided PC values.

        All inputs are numpy arrays of shape (n,)
        """

        P, dP = [], []
        for i in range(len(PC1)):
            x = np.array([PC1[i], PC1[i]+dPC1[i], PC1[i]-dPC1[i], PC1[i], PC1[i]])+self.P_solar[0][0]
            y = np.array([PC2[i], PC2[i], PC2[i], PC2[i]+dPC2[i], PC2[i]-dPC2[i]])+self.P_solar[0][1]
            o = []
            for jj in range(len(x)):
                o.append(self._getColour(x[jj], y[jj]))
            o = np.array(o)
            (g, j) = o[:,0], o[:,1]
            P.append([g[0], j[0]])
            dP.append([(np.max(x)-np.min(x))/2.0/1.414, (np.max(y)-np.min(y))/2.0/1.414])

        P = np.array(P)
        dP = np.array(dP)

        return (P, dP)


